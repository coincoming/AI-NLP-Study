# 基于训练好的词向量模型进行聚类
# 聚类采用Kmeans算法，并按照类内距离排序

import math
import jieba
import numpy as np
from gensim.models import Word2Vec
from sklearn.cluster import KMeans
from collections import defaultdict


# 加载训练好的模型
def load_word2vec_model(path):
    model = Word2Vec.load(path)
    return model


# 加载句子数据
def load_sentence(path):
    sentences = set()
    with open(path, encoding="utf8") as f:
        for line in f:
            sentence = line.strip()
            sentences.add(" ".join(jieba.cut(sentence)))
    print("获取句子数量：", len(sentences))
    return sentences


# 将文本向量化
def sentences_to_vectors(sentences, model):
    vectors = []
    for sentence in sentences:
        words = sentence.split()
        vector = np.zeros(model.vector_size)
        valid_word_count = 0
        for word in words:
            try:
                vector += model.wv[word]
                valid_word_count += 1
            except KeyError:
                continue
        if valid_word_count > 0:
            vector /= valid_word_count
        vectors.append(vector)
    return np.array(vectors)


def main():
    model = load_word2vec_model("model.w2v")  # 修改为你的模型路径
    sentences = list(load_sentence("titles.txt"))  # 转换为列表保持索引顺序
    vectors = sentences_to_vectors(sentences, model)

    n_clusters = int(math.sqrt(len(sentences)))
    print("指定聚类数量：", n_clusters)

    kmeans = KMeans(n_clusters, n_init=10)  # 明确设置n_init避免警告
    kmeans.fit(vectors)

    # 获取每个簇的中心点
    centroids = kmeans.cluster_centers_

    # 存储每个簇的(句子, 距离中心距离)元组
    cluster_dict = defaultdict(list)

    # 计算每个句子到其簇中心的距离
    for i, (sentence, label) in enumerate(zip(sentences, kmeans.labels_)):
        vec = vectors[i]
        centroid = centroids[label]
        distance = np.linalg.norm(vec - centroid)  # 欧氏距离
        cluster_dict[label].append((sentence, distance))

    # 为每个类计算距离统计量
    cluster_stats = {}
    for label, items in cluster_dict.items():
        distances = [d for _, d in items]
        cluster_stats[label] = {
            "min": min(distances),
            "max": max(distances),
            "avg": sum(distances) / len(distances)
        }

    # 按类内距离排序并输出结果
    for label, items in cluster_dict.items():
        # 按距离从小到大排序 (距离越小越接近中心)
        sorted_items = sorted(items, key=lambda x: x[1])

        stats = cluster_stats[label]
        print(
            f"\ncluster {label} (类内评估: 平均距离={stats['avg']:.4f}, 最大距离={stats['max']:.4f}, 最小距离={stats['min']:.4f})")
        print("=" * 70)
        print("最接近中心的10个样本:")

        for j, (sentence, distance) in enumerate(sorted_items[:10]):  # 取前10个最近的
            print(f"{j + 1}. {sentence.replace(' ', '')} \t[距离: {distance:.4f}]")


if __name__ == "__main__":
    main()

